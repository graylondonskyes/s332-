const fs = require('fs');
const path = require('path');
const { fail, ok, repoPath, writeJson } = require('./lib');
const { summarizeCatalogState, upsertCatalogTitle, activateCatalogTitle, deriveWorkspaceTitle, emptyCatalogState } = require('../platform/catalog');
const skydocx = JSON.parse(fs.readFileSync(repoPath('fixtures','publishing','skydocx-workspace.json'), 'utf8'));
const skyeblog = JSON.parse(fs.readFileSync(repoPath('fixtures','publishing','skyeblog-workspace.json'), 'utf8'));
let catalog = emptyCatalogState();
catalog = upsertCatalogTitle(catalog, skydocx, { titleId:'title_docx', titleName:'Sovereign Author Publishing OS' });
catalog = upsertCatalogTitle(catalog, skyeblog, { titleId:'title_blog', titleName:'SkyeBlog Command' });
if (catalog.titles.length !== 2) fail('[multi-title-catalog] FAIL :: title-count');
if (catalog.analytics.titles_count !== 2) fail('[multi-title-catalog] FAIL :: analytics-title-count');
if (catalog.analytics.by_mode.skydocx !== 1 || catalog.analytics.by_mode.skyeblog !== 1) fail('[multi-title-catalog] FAIL :: by-mode');
if (deriveWorkspaceTitle(skydocx) !== 'Sovereign Author Publishing OS') fail('[multi-title-catalog] FAIL :: derive-docx');
if (deriveWorkspaceTitle(skyeblog) !== 'SkyeBlog Command') fail('[multi-title-catalog] FAIL :: derive-blog');
const activated = activateCatalogTitle(catalog, 'title_docx');
if (activated.workspace.mode !== 'skydocx') fail('[multi-title-catalog] FAIL :: activate-mode');
if (!activated.workspace.files['manuscript.md']) fail('[multi-title-catalog] FAIL :: activate-files');
const summary = summarizeCatalogState(activated.state);
if (summary.active_title_name !== 'Sovereign Author Publishing OS') fail('[multi-title-catalog] FAIL :: summary-active');
const artifactDir = repoPath('artifacts','catalog-state');
fs.mkdirSync(artifactDir, { recursive: true });
writeJson(path.join(artifactDir, 'manifest.json'), { generated_at: new Date().toISOString(), ok: true, active_title_id: summary.active_title_id, summary, title_names: summary.title_names });
ok('[multi-title-catalog] PASS (10 vectors)');
